package bean_request;

public class Student {
	private String name;
	private int rollNo;
	
	public Student() {
		this.name = "Aman";
		this.rollNo = 137;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getRollNo() {
		return rollNo;
	}

	public void setRollNo(int rollNo) {
		this.rollNo = rollNo;
	}
	
	
}
